class Expense {
  final String id;
  final String name;
  final String title;
  final double amount;
  final DateTime date;
  final String category;
  final String description;

  Expense({
    required this.id,
    required this.name,
    required this.title,
    required this.amount,
    required this.date,
    required this.category,
    required this.description,
  });
}
